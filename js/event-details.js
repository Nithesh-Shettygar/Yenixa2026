        // Function to set dynamic background
        function setDynamicBackground(eventName) {
            const bgContainer = document.getElementById('dynamicBackground');
            bgContainer.innerHTML = '';

            // Get the background image path from event data or use default
            const eventData = completeEventsData[eventName];
            const bgPath = eventData?.backgroundImage ||
                `../assets/backgrounds/${eventName.toLowerCase().replace(/[^a-z0-9]/g, '_')}_bg.jpg`;

            // Create and add the background image
            const bgImage = document.createElement('img');
            bgImage.className = 'background-image active';
            bgImage.src = bgPath;
            bgImage.alt = `${eventName} Background`;

            // Add fallback styling in case image doesn't load
            bgImage.onerror = function () {
                // If the specific background fails to load, use a default background
                this.src = '../assets/backgrounds/past_future.jpeg';
                console.log(`Background for ${eventName} not found, using default.`);
            };

            bgContainer.appendChild(bgImage);
        }

